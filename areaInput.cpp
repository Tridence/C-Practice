//area of a rectangle
#include <iostream>
using namespace std;

int main(){
    //function declaration
    int length, width, area;

    //request input
    cout<<"Enter the length "<<endl;
    cin>>length;
    cout<< "Enter the width"<<endl;
    cin>>width;

    //calculate area
    area = length*width;

    //output
    cout<< "The area of the rectangle is: "<<endl<<area;

    return 0;




}